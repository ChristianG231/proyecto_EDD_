    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
if (cmbOrigen.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cargue el archivo primero.");
            return;
        }

        String inicioId = cmbOrigen.getSelectedItem().toString();
        Clases.AnalizadorConectividad analizador = new Clases.AnalizadorConectividad();
        Edd.ListaEnlazada<Neurona> aisladas = analizador.obtenerZonasAisladas(miRed, inicioId);

        dibujarRed(aisladas, null); // Manda las aisladas para pintarlas de rojo

        if (aisladas.estaVacia()) {
            txtEstado.setText("Resultado: Red totalmente conectada.");
        } else {
            txtEstado.setText("Alerta: Hay " + aisladas.getTamaño() + " nodos aislados desde " + inicioId);
        }        
    }//GEN-LAST:event_jButton3ActionPerformed

