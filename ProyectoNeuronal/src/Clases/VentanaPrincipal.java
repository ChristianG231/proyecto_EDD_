    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
   if (cmbOrigen.getSelectedItem() == null || cmbDestino.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione los dos puntos.");
            return;
        }

        String orig = cmbOrigen.getSelectedItem().toString();
        String dest = cmbDestino.getSelectedItem().toString();

        Clases.CalculadorRutaOptima dijkstra = new Clases.CalculadorRutaOptima();
        Edd.ListaEnlazada<Neurona> caminoVerde = dijkstra.calcularRuta(miRed, orig, dest);

        if (caminoVerde == null || caminoVerde.estaVacia()) {
            txtEstado.setText("No existe una ruta entre " + orig + " y " + dest);
            dibujarRed(null, null); 
        } else {
            dibujarRed(null, caminoVerde); // Manda el camino para pintarlo de verde
            txtEstado.setText("Ruta mas corta encontrada.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

