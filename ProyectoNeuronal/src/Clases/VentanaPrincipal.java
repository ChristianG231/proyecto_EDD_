// Metodo para llenar los combobox con los IDs
    private void actualizarCombos() {
        cmbOrigen.removeAllItems();
        cmbDestino.removeAllItems();
        
        Edd.Nodo<Neurona> aux = miRed.getListaTodasLasNeuronas().getCabeza();
        while (aux != null) {
            cmbOrigen.addItem(aux.getDato().getId());
            cmbDestino.addItem(aux.getDato().getId());
            aux = aux.getSiguiente();
        }
    }

    // Metodo para pintar el grafo en la pantalla de NetBeans
    public void dibujarRed(Edd.ListaEnlazada<Neurona> rojas, Edd.ListaEnlazada<Neurona> verdes) {
        Graph grafo = new SingleGraph("Red");
        
        // Estilos basicos (Estilo de estudiante)
        grafo.setAttribute("ui.stylesheet", 
            "node { fill-color: blue; size: 25px; text-color: white; } " +
            "node.aislada { fill-color: red; size: 30px; } " + 
            "node.ruta { fill-color: green; size: 30px; } " +    
            "edge { fill-color: black; } " +
            "edge.ruta { fill-color: green; size: 3px; }");      

        // 1. Agregar todos los nodos del grafo
        Edd.Nodo<Neurona> aux = miRed.getListaTodasLasNeuronas().getCabeza();
        while (aux != null) {
            String id = aux.getDato().getId();
            Node nodoNuevo = grafo.addNode(id);
            nodoNuevo.setAttribute("ui.label", id);
            
            // Si el analisis dice que es aislada va roja, si es de ruta va verde
            if (rojas != null && rojas.contiene(aux.getDato())) {
                nodoNuevo.setAttribute("ui.class", "aislada");
            } else if (verdes != null && verdes.contiene(aux.getDato())) {
                nodoNuevo.setAttribute("ui.class", "ruta");
            }
            aux = aux.getSiguiente();
        }

        // 2. Agregar todas las aristas (conexiones)
        Edd.Nodo<ConexionSinaptica> auxConex = miRed.getTodasLasConexiones().getCabeza();
        while (auxConex != null) {
            ConexionSinaptica c = auxConex.getDato();
            String idOri = c.getOrigen().getId();
            String idDes = c.getDestino().getId();
            
            try {
                Edge arista = grafo.addEdge(idOri + "-" + idDes, idOri, idDes, true);
                arista.setAttribute("ui.label", c.getDistancia());
                
                // Pintar la linea de verde si pertenece a la ruta optima
                if (verdes != null && verdes.contiene(c.getOrigen()) && verdes.contiene(c.getDestino())) {
                    arista.setAttribute("ui.class", "ruta");
                }
            } catch(Exception e) {} 
            
            auxConex = auxConex.getSiguiente();
        }

        // 3. Montar el visor de GraphStream dentro de tu panel de NetBeans (Con P mayuscula)
        Viewer visor = new SwingViewer(grafo, Viewer.ThreadingModel.GRAPH_IN_ANOTHER_THREAD);
        visor.enableAutoLayout(); 
        ViewPanel panelGraph = (ViewPanel) visor.addDefaultView(false);
        
        PanelVisorGrafo.removeAll();
        PanelVisorGrafo.add(panelGraph, BorderLayout.CENTER);
        PanelVisorGrafo.revalidate();
        PanelVisorGrafo.repaint();
    }
